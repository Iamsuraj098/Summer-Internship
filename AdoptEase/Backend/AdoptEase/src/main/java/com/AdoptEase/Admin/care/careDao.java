package com.AdoptEase.Admin.care;

public class careDao {
	String name;
	String Quali;
	int fees;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getQuali() {
		return Quali;
	}
	public void setQuali(String quali) {
		Quali = quali;
	}
	public int getFees() {
		return fees;
	}
	public void setFees(int fees) {
		this.fees = fees;
	}
}
